# Variáveis em Python
# Não há necessidade de declará-las no ínicio do programa
# Basta criar no mais do algoritmo com a atribuição

teste = "Sávio"
print(teste) # Imprime a string "Eduardo"
print(type(teste)) # Imprime o tipo da variável, string
teste = 10
print(teste) # Imprime o número 10
print(type(teste)) # Imprime o tipo da variável, int
teste = 1.74
print(teste) # Imprime o número (1, 74)
print(type(teste)) # Imprime o tipo da variável, que é float
teste = True
print(teste) # Imprime a string True
print(type(teste)) # Imprime o tipo da variável, que é bool
